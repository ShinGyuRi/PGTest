package kr.innisfree.playgreen.common.view;


import android.content.Context;
import android.graphics.Typeface;
import android.util.AttributeSet;
import android.widget.TextView;

import com.moyusoft.util.Definitions;


public class InnisfreeGothicBoldTextView extends TextView {


    public InnisfreeGothicBoldTextView(Context context, AttributeSet attrs) {
        super(context, attrs);
        if (Definitions.InnisfreeGothicBold == null) {
            Definitions.InnisfreeGothicBold = Typeface.createFromAsset(context.getAssets(), "InnisfreeGothicB.ttf");
        }
        if(Definitions.InnisfreeGothicBold !=null)
            setTypeface(Definitions.InnisfreeGothicBold);
        //setLineSpacing(TypedValue.applyDimension(TypedValue.COMPLEX_UNIT_DIP, 5.0f, getResources().getDisplayMetrics()), 1.0f);

    }

    public InnisfreeGothicBoldTextView(Context context) {
        super(context);
        if (Definitions.InnisfreeGothicBold == null) {
            Definitions.InnisfreeGothicBold = Typeface.createFromAsset(context.getAssets(), "InnisfreeGothicB.ttf");
        }
        if(Definitions.InnisfreeGothicBold !=null)
            setTypeface(Definitions.InnisfreeGothicBold);
        //setLineSpacing(TypedValue.applyDimension(TypedValue.COMPLEX_UNIT_DIP, 5.0f, getResources().getDisplayMetrics()), 1.0f);
    }

    public InnisfreeGothicBoldTextView(Context context, AttributeSet attrs, int defStyle) {
        super(context, attrs, defStyle);
        if (Definitions.InnisfreeGothicBold == null) {
            Definitions.InnisfreeGothicBold = Typeface.createFromAsset(context.getAssets(), "InnisfreeGothicB.ttf");
        }
        if(Definitions.InnisfreeGothicBold !=null)
            setTypeface(Definitions.InnisfreeGothicBold);
        //setLineSpacing(TypedValue.applyDimension(TypedValue.COMPLEX_UNIT_DIP, 5.0f, getResources().getDisplayMetrics()), 1.0f);
    }


}
